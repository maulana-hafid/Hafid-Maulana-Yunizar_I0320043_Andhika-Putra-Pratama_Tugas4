x = 4
y = 11

print('nilai :',x,' , biner :',format(x,'08b'))
print('nilai :',y,' , biner :',format(y,'08b'))

print('==============(|)=====================')
print('nilai :',x,' , biner :',format(x,'08b'))
print('nilai :',y,' , biner :',format(y,'08b'))
print('-------------(4|11)-------------------')
print('nilai :',x|y,' , biner :',format(x|y,'08b'))

print('==============(>>)====================')
print('nilai :',x,' , biner :',format(x,'08b'))
print('nilai :',y,' , biner :',format(y,'08b'))
print('--------------(4>>11)-----------------')
print('nilai : ',x>>y,' , biner :',format(x>>y,'08b'))

print('==============(^)=====================')
print('nilai :',x,' , biner :',format(x,'08b'))
print('nilai :',y,' , biner :',format(y,'08b'))
print('--------------(4^11)------------------')
print('nilai : ',x^y,' , biner :',format(x^y, '08b'))

print('==============(~)=====================')
print('nilai :',x,' , biner :',format(x,'08b'))
print('nilai :',y,' , biner :',format(y,'08b'))
print('--------------(~x)--------------------')
print('nilai : ',~x,', biner :',format(~x,'08b'))

print('==============(&)=====================')
print('nilai :',x,' , biner :',format(x,'08b'))
print('nilai :',y,' , biner :',format(y,'08b'))
print('---------------(y&x)------------------')
print('nilai : ',y&x,' , biner :',format(y&x,'08b'))